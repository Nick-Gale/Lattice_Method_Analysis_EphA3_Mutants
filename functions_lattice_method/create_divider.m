function [divider] = create_divider(RC, ML)
%%---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
%% Make a simple fit based on the axis of variation
%%---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
%%
    divider = mean(RC);


%%---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
%% if a line fit is required:
%%---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
%%
    %we are fitting on the RC = f(ML) dimension, but we calculate on the ML = f^-1(RC) so we need to invert the fit
    [line, struct] = polyfit(ML, RC, 1);
    intersect = line(2);
    gradient = line(1);
    %alternatively just use the mean of the x positions
    intersect = mean(RC);
end